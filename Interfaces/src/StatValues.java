public class StatValues {

    int level;
    int healthPoints;
    int manaPoints;
    int strength;
    int intelligence;
    int agility;

    @Override
    public String toString() {
        return "StatValues{" +
                "level=" + level +
                ", healthPoints=" + healthPoints +
                ", manaPoints=" + manaPoints +
                ", strength=" + strength +
                ", intelligence=" + intelligence +
                ", agility=" + agility +
                '}';
    }
}
