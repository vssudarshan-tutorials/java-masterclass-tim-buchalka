public class Switch {

    public static void main(String[] args) {

        char a = 'A';
        switch(a) {

            case 'A':
                System.out.println("Character is " + a);
                break;
            case 'B':
                System.out.println("Character is " + a);
                break;
            case 'C':
                System.out.println("Character is " + a);
                break;
            case 'D':
                System.out.println("Character is " + a);
                break;
            case 'E':
                System.out.println("Character is " + a);
                break;
            default:
                System.out.println("Character is not A, B, C, D , E. It is " + a + ".");
                break;

        }
    }


}
